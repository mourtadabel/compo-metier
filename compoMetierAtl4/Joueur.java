package compoMetierAtl4;

public class Joueur {
	private String prenom;
	private int cash;
	
	public Joueur() {
	}

	public String getPrenom() {
		return prenom;
	}

	public void setPrenom(String prenom) {
		this.prenom = prenom;
	}

	public int getCash() {
		return cash;
	}

	public void setCash(int cash) {
		this.cash = cash;
	}
	
	

}
